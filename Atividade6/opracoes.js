var  num1 = prompt("Digite o primeiro Numero");
var num2 = prompt("Digite o segundo numero");
soma = parseFloat(num1) + parseFloat(num2);
subtracao = parseFloat(num1) - parseFloat(num2);
mult = parseFloat(num1) * parseFloat(num2);
divisao = parseFloat(num1) / parseFloat(num2);
divresto = parseFloat(num1) % parseFloat(num2);

alert(num1 + " + " + num2 + " = " + soma);
alert(num1 + " - " + num2 + " = " + subtracao);
alert(num1 + " * " + num2 + " = " + mult);
alert(num1 + " / " + num2 + " = " + divisao);
alert(num1 + " % " + num2 + " = " + divresto);
